    private void btnVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificarActionPerformed
        try {
            respuesta = cine.BusquedaSecuencial(txtNombre.getText());
            if (respuesta == -1) {
                txtNombre.setEditable(false);
                txtCiudad.setEditable(true);
                txtDireccion.setEditable(true);
                btnRegistrar.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(this, "El registro ya esxiste.");
            }
        } catch (IOException ex) {
            Logger.getLogger(Registrar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnVerificarActionPerformed

    public void IngresarDatos() throws IOException {
        int res = JOptionPane.showConfirmDialog(this, "Â¿Seguro de guardar el registro?");
        if (res == 0) {
            cine.setNombre(txtNombre.getText());
            cine.setCiudad(txtCiudad.getText());
            cine.setDireccion(txtDireccion.getText());
            cine.setActivo((byte) 1);
            cine.IngresarRegistro();
            txtNombre.setText("");
            txtCiudad.setText("");
            txtDireccion.setText("");
        }
    }

